//CarND-PID-Control-Project/PID.h
#ifndef PID_H
#define PID_H

class PID {
public:
  /*
  * Errors
  */
  double p_error_;
  double i_error_;
  double d_error_;

  /*
  * Coefficients
  */ 
  double Kp_;
  double Ki_;
  double Kd_;

  unsigned long packet_count;
  /*
  * Constructor
  */
  PID();

  /*
  * Destructor.
  */
  virtual ~PID();

  /*
  * Initialize PID.
  */
  void Init(double Kp, double Ki, double Kd);

  /*
  * Update the PID error variables given cross track error.
  */
  void UpdateError(double cte);

  /*
  * Calculate the total PID error.
  */
  double TotalError();
  double Throttle (double goal_throttle) ;
  double steeringAngle ();
  void DisplayKvalues() ;
  void ChangeKValues(double Kp, double Ki, double Kd);
};

#endif /* PID_H */
